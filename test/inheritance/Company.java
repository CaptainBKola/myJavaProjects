package inheritance;

public class Company {

    public String nameOfBusiness;
    public int yearOfBusiness;

    public int getYearOfBusiness() {
        return yearOfBusiness;
    }

    public void setYearOfBusiness(int yearOfBusiness) {
        this.yearOfBusiness = yearOfBusiness;
    }

    public String getNameOfBusiness() {
        return nameOfBusiness;
    }

    public void setNameOfBusiness(String nameOfBusiness) {
        this.nameOfBusiness = nameOfBusiness;
    }
}

